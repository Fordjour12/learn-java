/**
 * datatype
 */
public class datatype {

    public static void main(String[] args) {

        byte flag = -128;
        short lgs = 3484;
        int ngs = 32529;
        long lsg = -423_322_000_00L;
        float ft = -34.3f;
        double dbd = 34.0;
        char mych = '\u0039';

        String myNa = "Mika";


        System.out.println("byte: " + flag);
        System.out.println("short: " + lgs);
        System.out.println("int: " + ngs);
        System.out.println("long: " + lsg);
        System.out.println("float: " + ft);
        
        System.out.println("double: "+ dbd);

        System.out.println("char: "+ mych);

        System.out.println("string: "+ myNa);


        
    }
}
